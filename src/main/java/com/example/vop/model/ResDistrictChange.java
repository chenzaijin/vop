package com.example.vop.model;

public class ResDistrictChange {

    private String regionID;

    private String regionName;

    private String regionLT;

    private String holdCol;

    public String getRegionID() {
        return regionID;
    }

    public void setRegionID(String regionID) {
        this.regionID = regionID;
    }

    public String getRegionName() {
        return regionName;
    }

    public void setRegionName(String regionName) {
        this.regionName = regionName;
    }

    public String getRegionLT() {
        return regionLT;
    }

    public void setRegionLT(String regionLT) {
        this.regionLT = regionLT;
    }

    public String getHoldCol() {
        return holdCol;
    }

    public void setHoldCol(String holdCol) {
        this.holdCol = holdCol;
    }
}
